    public editlowongan(String usernameParam, int id_lowonganParam) {
        username = usernameParam;
        id_lowongan = id_lowonganParam;
        handleMySQL db = new handleMySQL();
        Connection conn = db.connect();
        try {
            String query = "SELECT * FROM tb_lowongan WHERE id_lowongan = ?";
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setInt(1, id_lowongan);
            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                initComponents();
                jTextField2.setText(rs.getString("nama_lowongan"));
                jTextArea1.setText(rs.getString("deskripsi"));
                jSpinner1.setValue(rs.getInt("gaji"));
                jRadioButton1.setSelected(rs.getBoolean("enabled"));
                jComboBox1.setSelectedItem(rs.getString("kategori"));
                jTextField3.setText(rs.getString("daerah"));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

