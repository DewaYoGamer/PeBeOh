    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        handleMySQL db = new handleMySQL();
        Connection conn = db.connect();
        if (jTextField2.getText().equals("") || jTextArea1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Nama lowongan dan deskripsi tidak boleh kosong!");
            return;
        }
        try {
            String query = "UPDATE tb_lowongan SET nama_lowongan = ?, deskripsi = ?, gaji = ?, enabled = ?, kategori = ? , daerah = ? WHERE id_lowongan = ?";
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, jTextField2.getText());
            stmt.setString(2, jTextArea1.getText());
            stmt.setInt(3, (int) jSpinner1.getValue());
            stmt.setBoolean(4, jRadioButton1.isSelected());
            stmt.setString(5, jComboBox1.getSelectedItem().toString());
            stmt.setString(6, jTextField3.getText());
            stmt.setInt(7, id_lowongan);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Lowongan berhasil diperbaharui!");
            this.dispose();
            HomePagePerusahaan home = new HomePagePerusahaan(username, 1);
            home.setVisible(true);
            home.pack();
            home.setLocationRelativeTo(null);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int response = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus lowongan ini?", "Hapus Lowongan", JOptionPane.YES_NO_OPTION);
        if (response == JOptionPane.YES_OPTION) {
            handleMySQL db = new handleMySQL();
            Connection conn = db.connect();
            try {
                String query = "DELETE FROM tb_lowongan WHERE id_lowongan = ?";
                PreparedStatement stmt = conn.prepareStatement(query);
                stmt.setInt(1, id_lowongan);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Lowongan berhasil dihapus!");
                this.dispose();
                HomePagePerusahaan home = new HomePagePerusahaan(username, 1);
                home.setVisible(true);
                home.pack();
                home.setLocationRelativeTo(null);
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

